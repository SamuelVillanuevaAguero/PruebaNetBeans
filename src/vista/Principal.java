    MouseAdapter mouseAdapter = new MouseAdapter() {
        private Point initialClick;

        @Override
        public void mousePressed(MouseEvent e) {
            initialClick = e.getPoint();
            getComponent(e).setCursor(Cursor.getPredefinedCursor(Cursor.MOVE_CURSOR));
        }

        @Override
        public void mouseDragged(MouseEvent e) {
            // Get location of the window
            JFrame parent = (JFrame) SwingUtilities.getWindowAncestor(titleBar);
            int thisX = parent.getLocation().x;
            int thisY = parent.getLocation().y;

            // Determine how much the mouse moved since the initial click
            int xMoved = e.getX() - initialClick.x;
            int yMoved = e.getY() - initialClick.y;

            // Move window to this position
            int X = thisX + xMoved;
            int Y = thisY + yMoved;
            parent.setLocation(X, Y);
        }

        @Override
        public void mouseReleased(MouseEvent e) {
            getComponent(e).setCursor(Cursor.getDefaultCursor());
        }

        private Component getComponent(MouseEvent e) {
            return (Component) e.getSource();
        }
    };

