    private void bAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAgregarActionPerformed
        // TODO add your handling code here:
        String query = "MATCH (a:"+tipoNodo1.getSelectedItem().toString()+"{"+nombreAtributo1.getText()+": '"+valorAtributo1.getText()+"'}),"
                           + "(b:"+tipoNodo2.getSelectedItem().toString()+"{"+nombreAtributo2.getText()+": '"+valorAtributo2.getText()+"'})";
        
        String tipoR = tipoRelación.getSelectedItem().toString();
        
        if (!match(query+" RETURN a,b")) {
            JOptionPane.showMessageDialog(null, "No se encontraron coincidencias","Relaciones",JOptionPane.ERROR_MESSAGE);
            return;
        }

        switch (tipo) {
            case 1:

                String atributo;

                if (tipoRelación.getSelectedItem().toString().equals("FAMILIAR")) {
                    atributo = JOptionPane.showInputDialog("Ingresa el parentesco: ");
                    query += " CREATE (a)-[:" + tipoR + "{parentesco: '" + atributo + "'}]->(b)";
                } else {
                    atributo = JOptionPane.showInputDialog("Ingresa la fecha de inicio: ");
                    query += " CREATE (a)-[:" + tipoR + "{fecha_inicio: '" + atributo + "'}]->(b)";
                }

                break;
            case 2:
                //SEGUN SWITCH, SIRVE PARA VALIDAR LA PETICIÓN DE ATRIBUTOS DEL TIPO DE 2 DE RELACIÓN (PERSONA, OBJETO)    
                switch (tipoR) {
                    case "REGALA", "VENDE", "ADQUIERE" -> {
                        String fecha = JOptionPane.showInputDialog("Ingresa la fecha: ");
                        query += " CREATE (a)-[:" + tipoR + "{fecha: '" + fecha + "'}]->(b)";
                    }
                    case "POSEE" -> {
                        String fechaPosesion = JOptionPane.showInputDialog("Ingresa la fecha: ");
                        int costo = Integer.parseInt(JOptionPane.showInputDialog("Ingresa el costo: "));
                        query += " CREATE (a)-[:" + tipoR + "{fecha_posesion: '" + fechaPosesion + "', costo: "+costo+"}]->(b)";
                    }
                    default -> {
                        String fechaInicio = JOptionPane.showInputDialog("Ingresa la fehca inicio: ");
                        String fechaFin = JOptionPane.showInputDialog("Ingresa la fehca fin: ");
                        int costo = Integer.parseInt(JOptionPane.showInputDialog("Ingresa el costo: "));
                        query += " CREATE (a)-[:" + tipoR + "{fecha_inicio: '"+fechaInicio+"', fecha_fin: '"+fechaFin+"', costo_renta: "+costo+"}]->(b)";   
                    }
                }///////////////////////////////////////////////////////////////////////////////////////////////////////
                break;
            default:
        }
        insertarRelacion(query);
    }//GEN-LAST:event_bAgregarActionPerformed

