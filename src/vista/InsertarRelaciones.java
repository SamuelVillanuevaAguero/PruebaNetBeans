    private void crearLista(String lista[]) {
        tipoRelación.removeAllItems();

        for (int i = 0; i < lista.length; i++) {
            tipoRelación.addItem(lista[i]);
        }
    }

    private void seleccionaLista() {
        String tipo1 = tipoNodo1.getSelectedItem().toString();
        String tipo2 = tipoNodo2.getSelectedItem().toString();

        //Si los dos objetos son del mismo tipo, hay que determinar cual es el tipo de ambos nodos
        if (tipo1.equals(tipo2)) {
            if (tipo1.equals("Persona")) {
                crearLista(lista1);
                tipo = 1;
            } else {
                //
                tipoRelación.removeAllItems();
            }
            //Si los tipos de nodos son distintos, hay que determinar cual es el primer nodo
        } else {
            if (tipo1.equals("Persona")) {
                crearLista(lista2);
                tipo = 2;
            } else {
                //
                tipoRelación.removeAllItems();
            }
        }
    }
    
    private void insertarRelacion(String query) {
        try {
            Driver driver;
            driver = GraphDatabase.driver("bolt://localhost:7687", AuthTokens.basic("neo4j", "s,dycsj01"));
            Session session = driver.session();
            session.run(query).consume();
            
            nombreAtributo1.setText("");
            nombreAtributo2.setText("");
            valorAtributo1.setText("");
            valorAtributo2.setText("");
            
            JOptionPane.showMessageDialog(null, "La relación se insertó correctamente", "Relaciones", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se pudo insertar la relación", "Relaciones", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private boolean match(String match) {
        try {
            Driver driver;
            driver = GraphDatabase.driver("bolt://localhost:7687", AuthTokens.basic("neo4j", "s,dycsj01"));
            Session session = driver.session();
            Result r = session.run(match);

            return r.hasNext();

        } catch (Exception e) {
            
        }
        return false;
    }

